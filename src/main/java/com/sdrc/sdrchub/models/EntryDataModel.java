package com.sdrc.sdrchub.models;

public class EntryDataModel {

	private String author;
	private String comment;
	private String referenceLinks;
	private String subject;
	private String tags;
	private String videoLinks;
	
	
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getReferenceLinks() {
		return referenceLinks;
	}
	public void setReferenceLinks(String referenceLinks) {
		this.referenceLinks = referenceLinks;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
	public String getVideoLinks() {
		return videoLinks;
	}
	public void setVideoLinks(String videoLinks) {
		this.videoLinks = videoLinks;
	}
	
	
}
